using UnityEngine;

[AddComponentMenu("#NVJOB/Tools/ExampleCH")]


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


public class ExampleCH : MonoBehaviour
{
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    public GameObject exampleN, exampleS;


    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    void Awake()
    {
        //--------------

        exampleN.SetActive(true);
        exampleS.SetActive(!exampleN.activeSelf);

        //--------------
    }


    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    void LateUpdate()
    {
        //--------------

        if (Input.GetMouseButtonDown(1))
        {
            exampleN.SetActive(!exampleN.activeSelf);
            exampleS.SetActive(!exampleN.activeSelf);
        }

        //--------------
    }


    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}
